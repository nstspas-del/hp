import servicesData from '@/data/services.json';

export type ServiceItem = {
 slug: string;
 name: string;
 shortDescription?: string;
 priceFrom: number;
 duration?: string;
 features?: string[];
};

export type ServiceCategory = {
 slug: string;
 name: string;
 items: ServiceItem[];
};

export function getCategory(catSlug: string): ServiceCategory | undefined {
 return servicesData.categories.find((c) => c.slug === catSlug) as ServiceCategory | undefined;
}

export function getService(catSlug: string, serviceSlug: string): ServiceItem | undefined {
 const cat = getCategory(catSlug);
 return cat?.items.find((s) => s.slug === serviceSlug);
}

export function getCategoryLabel(catSlug: string): string {
 const labels: Record<string, string> = {
 'chip-tuning': 'Чип-тюнинг',
 'detailing': 'Детейлинг',
 'service': 'Автосервис',
 };
 return labels[catSlug] ?? catSlug;
}

